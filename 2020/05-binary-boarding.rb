class String
  def colorize(color_code)
    "\e[#{color_code}m#{self}\e[0m"
  end

  def red;    colorize(31); end
  def green;  colorize(32); end
  def yellow; colorize(33); end
end

input   = DATA.read.split
rows    = (0..127).to_a
columns = (0..7).to_a

def bsp(rows, chars)
  return rows.first if rows.size == 1

  head, *tail = chars
  left, right = rows.each_slice(rows.size/2).to_a

  case head
  when /F|L/ # lower half <-
    bsp(left, tail)
  when /B|R/ # upper half ->
    bsp(right, tail)
  end
end

seat_ids  = []
seat_id   = ->(row, column) { row * 8 + column }
plane     = ->(seat_id) { seat_id.divmod(8) }

input.each do |boarding_pass|
  row      = bsp(rows, boarding_pass.chars[0...7])
  column   = bsp(columns, boarding_pass.chars[7..10])
  seat_ids << seat_id[row, column]
end

puts "What is the highest seat ID on a boarding pass?"
puts seat_ids.max
# => 935


puts
puts "What is the ID of your seat?"
my_seat_id         = ((seat_ids.min..seat_ids.max).to_a - seat_ids).first
my_row, my_column  = plane[my_seat_id]

puts "#{my_seat_id} (@ row #{my_row}, column #{my_column})"
# => 743 (@ row 92, column 7)


PADDING    = 6
OFFSET     = 10
skip_lower = my_row - OFFSET
skip_upper = my_row + OFFSET

puts
rows.each do |row|
  # skip noise
  puts '(...)'.rjust(PADDING) if row == skip_lower - OFFSET
  next if (OFFSET..skip_lower).cover?(row)
  next if (skip_upper..rows.size-OFFSET).cover?(row)

  print "#{row} |".to_s.rjust(PADDING)
  columns.each do |column|
    current_seat_id = seat_id[row, column]

    if seat_ids.include?(current_seat_id)
      # taken
      print "#{current_seat_id}".rjust(PADDING).red
    elsif my_seat_id == current_seat_id
      # ohai my seat!
      print "->#{my_seat_id}".rjust(PADDING).green
    else
      # free or doesn't exist
      print "#{current_seat_id}".rjust(PADDING).green
    end
  end
  puts
end

