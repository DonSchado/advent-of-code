input = DATA.read.split("\n")

VALUES = {
  rock:     1,
  paper:    2,
  scissors: 3,
}

MAPPING = {
  'A' => :rock,
  'B' => :paper,
  'C' => :scissors,
  'X' => :rock,
  'Y' => :paper,
  'Z' => :scissors,
}

guide = input.map do |turn|
  opp, me = turn.split
  [MAPPING[opp], MAPPING[me]]
end


# Rock defeats Scissors, Scissors defeats Paper, and Paper defeats Rock
def win(a, b)
  case [a, b].sort
  when [:rock, :scissors]  then :rock
  when [:paper, :scissors] then :scissors
  when [:paper, :rock]     then :paper
  end
end

def cheat(a, b)
  if b == :rock # lose
    [:rock, :scissors, :paper].find { |sign| win(sign, a) == a }
  elsif b == :paper # need draw
    return a
  elsif b == :scissors # win
    [:rock, :scissors, :paper].find { |sign| win(sign, a) == sign }
  end
end


def run(guide, cheating: false, score: 0)
  # 0 if you lost, 3 if the round was a draw, and 6 if you won
  guide.each do |turn|
    opp   =  turn.first
    me    =  cheating ? cheat(*turn) : turn.last
    score += VALUES[me]

    case win(opp, me)
    when me  then score += 6
    when nil then score += 3
    end
  end

  score
end

